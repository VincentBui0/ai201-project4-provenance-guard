@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    data = request.get_json(force=True, silent=True)
    if not data:
        return jsonify({"error": "Invalid or missing JSON body"}), 400

    text = data.get("text", "").strip()
    creator_id = data.get("creator_id", "").strip()

    if not text:
        return jsonify({"error": "Missing required field: text"}), 400
    if not creator_id:
        return jsonify({"error": "Missing required field: creator_id"}), 400

    content_id = str(uuid.uuid4())
    word_count = len(text.split())

    llm = llm_classify(text)
    stylo = stylometric_score(text)
    confidence = combine_scores(llm, stylo, word_count)

    if confidence >= 0.65:
        attribution = "likely_ai"
    elif confidence >= 0.36:
        attribution = "uncertain"
    else:
        attribution = "likely_human"

    label = generate_label(confidence)

    entry = {
        "content_id": content_id,
        "creator_id": creator_id,
        "timestamp": datetime.datetime.utcnow().isoformat() + "Z",
        "attribution": attribution,
        "confidence": confidence,
        "llm_score": llm,
        "stylometric_score": stylo,
        "status": "classified",
        "entry_type": "classification",
        "appeal_reasoning": None,
    }
    write_log(entry)

    return jsonify({
        "content_id": content_id,
        "attribution": attribution,
        "confidence": confidence,
        "llm_score": llm,
        "stylometric_score": stylo,
        "label_variant": label["label_variant"],
        "label_text": label["label_text"],
    })


@app.route("/appeal", methods=["POST"])
def appeal():
    data = request.get_json(force=True, silent=True)
    if not data:
        return jsonify({"error": "Invalid or missing JSON body"}), 400

    content_id = data.get("content_id", "").strip()
    creator_id = data.get("creator_id", "").strip()
    reasoning = data.get("creator_reasoning", "").strip()

    if not content_id:
        return jsonify({"error": "Missing required field: content_id"}), 400
    if not creator_id:
        return jsonify({"error": "Missing required field: creator_id"}), 400
    if not reasoning or len(reasoning) < 20:
        return jsonify({"error": "creator_reasoning must be at least 20 characters"}), 400

    original = get_entry(content_id)
    if not original:
        return jsonify({"error": "content_id not found"}), 404

    if original["creator_id"] != creator_id:
        return jsonify({"error": "creator_id does not match original submission"}), 403

    if original["status"] == "under_review":
        return jsonify({"error": "An appeal is already under review for this content"}), 409

    update_status(content_id, "under_review")

    appeal_entry = {
        "content_id": content_id,
        "creator_id": creator_id,
        "timestamp": datetime.datetime.utcnow().isoformat() + "Z",
        "attribution": original["attribution"],
        "confidence": original["confidence"],
        "llm_score": original["llm_score"],
        "stylometric_score": original["stylometric_score"],
        "status": "under_review",
        "entry_type": "appeal",
        "appeal_reasoning": reasoning,
    }
    write_log(appeal_entry)

    return jsonify({
        "content_id": content_id,
        "status": "under_review",
        "message": "Your appeal has been received and will be reviewed. No automated re-classification will occur.",
    })


@app.route("/log", methods=["GET"])
def log():
    n = request.args.get("n", 20, type=int)
    return jsonify({"entries": get_log(n)})


@app.route("/appeals", methods=["GET"])
def appeals_queue():
    conn = sqlite3.connect("audit.db")
    rows = conn.execute(
        "SELECT * FROM log WHERE entry_type = 'appeal' ORDER BY timestamp DESC"
    ).fetchall()
    conn.close()
    cols = ["content_id", "creator_id", "timestamp", "attribution",
            "confidence", "llm_score", "stylometric_score", "status",
            "entry_type", "appeal_reasoning"]
    return jsonify({"appeals": [dict(zip(cols, row)) for row in rows]})


